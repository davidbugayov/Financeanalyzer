package com.davidbugayov.financeanalyzer.presentation.history.model

/**
 * Типы группировки транзакций в истории.
 * Определяет, как транзакции будут сгруппированы в списке.
 * Используется в компоненте GroupingChips для выбора типа группировки.
 */
enum class GroupingType {

    /** Группировка по дням (показывает транзакции за каждый день) */
    DAY,

    /** Группировка по неделям (объединяет транзакции за неделю) */
    WEEK,

    /** Группировка по месяцам (объединяет транзакции за месяц) */
    MONTH
} 
