package com.davidbugayov.financeanalyzer.presentation.history.model

/**
 * Типы периодов для фильтрации транзакций в истории.
 * Используется в диалоге выбора периода и для фильтрации списка транзакций.
 */
enum class PeriodType {

    /** Все транзакции без ограничения по дате */
    ALL,

    /** Транзакции за последний месяц */
    MONTH,

    /** Транзакции за последний квартал (3 месяца) */
    QUARTER,

    /** Транзакции за последние полгода */
    HALF_YEAR,

    /** Транзакции за последний год */
    YEAR,

    /** Произвольный период, задаваемый пользователем */
    CUSTOM
} 